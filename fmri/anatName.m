function name = anatName(subjInitials)
% commonly used anatomy names
initials = {'JG' 'SP' 'EM' 'AS'...
            'TH' 'AR' 'MN' 'DF'...
            'MG' 'MH' 'JC' 'JJ'...
            'JP' 'JW' 'MZ'};
anats = {'jg201311' 'sp201803' 'em201611' 'awms201311'...
        'th201810' 'ar201810' 'mn201701' 'df201801'...
        'mg201708' 'mjh201909' 'jc201910' 'jj201909'...
        'jp201502' 'jw201810' 'mz201904'};

try
name = anats{ismember(initials,subjInitials)};
catch
  name = anats{ismember(lower(initials),subjInitials)};  
end
end

